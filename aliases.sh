# Here are some handy-dandy aliases
# for both zsh and bash

#[CONFIG]
alias ev='vim ~/.vimrc'
alias ez='vim ~/.zshrc'
alias ea='vim ~/dotfiles/aliases.sh'

#[MISC]
alias c=clear
alias h=history
alias tr='trash'
alias l='ls -l'
alias gsp='grep -F'

#[TMUX]
alias t=tmux

#[GIT]
alias git=/usr/local/bin/git
alias gp='git push'
alias gu='git up'
alias gs='git status -sb'
alias gl='git log --graph'
alias ga='git add'
alias gb='git branch'
alias gc='git commit'
alias gd='git diff'
alias gsh='git show'
alias gk='git checkout'
alias gls='git ls-files'
alias gi='git ignored'
alias gf='git fetch'

#[GRUNT]
alias g='grunt'
alias gcs='grunt && grunt server' # compile & watch

#[VIM]
alias vim=/Applications/MacVim.app/Contents/MacOS/Vim # use the MacVim binary

#[TEMP]
alias qa='python -mwebbrowser http://ncsbn.sparkboxqa.com'
